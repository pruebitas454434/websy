// Función para obtener traducciones
export const getTranslation = (lang, key) => {
  const keys = key.split('.');
  let translation = translations[lang];
  
  for (const k of keys) {
    translation = translation?.[k];
  }
  
  return translation || translations.es[key] || key;
};